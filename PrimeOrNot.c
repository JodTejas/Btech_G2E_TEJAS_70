#include <stdio.h>

int main() {
    int num, i, x = 0;

    printf("Enter a number: ");
    scanf("%d", &num);

    if(num <= 1) {
        printf("Not a Prime Number\n");
    } else {
        for(i = 2; i <= num / 2; i++) {
            if(num % i == 0) {
                x = 1;
                break;
            }
        }

        if(x == 0)
            printf("Prime Number\n");
        else
            printf("Not a Prime Number\n");
    }

    return 0;
}